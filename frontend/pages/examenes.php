        <section class="examenes-grid">
            <article class="examen-card">
                <h2>Exámenes de sangre</h2>
                <p>
                    Incluyen hemograma completo, perfil renal, hepático y electrolitos.
                    Permiten evaluar órganos internos, defensas, anemia, infecciones
                    y seguimiento de tratamientos prolongados.
                </p>
            </article>

            <article class="examen-card">
                <h2>Exámenes de orina</h2>
                <p>
                    Ayudan a detectar infecciones urinarias, presencia de cristales,
                    problemas renales y alteraciones metabólicas como la diabetes.
                </p>
            </article>

            <article class="examen-card">
                <h2>Exámenes de heces</h2>
                <p>
                    Se utilizan para identificar parásitos intestinales, presencia de sangre oculta
                    y otros cambios que afectan la digestión y la absorción de nutrientes.
                </p>
            </article>

            <article class="examen-card">
                <h2>Rayos X (radiografías)</h2>
                <p>
                    Imágenes que permiten observar huesos, articulaciones, tórax y abdomen.
                    Son útiles en fracturas, problemas respiratorios, cuerpos extraños y más.
                </p>
            </article>

            <article class="examen-card">
                <h2>Ecografía abdominal</h2>
                <p>
                    Estudio por ultrasonido que muestra en tiempo real órganos como hígado,
                    riñones, vejiga y aparato reproductor, sin causar dolor al paciente.
                </p>
            </article>

            <article class="examen-card">
                <h2>Electrocardiograma (ECG)</h2>
                <p>
                    Registra la actividad eléctrica del corazón para detectar arritmias
                    y valorar la función cardíaca antes de cirugías o en pacientes geriátricos.
                </p>
            </article>
        </section>

        <section class="examenes-nota">
